fn render_magic_shim(
    parent_original: &str,
    fields: &[FieldDecl],
    parent: &StructDecl,
    anons_by_parent: &std::collections::BTreeMap<String, Vec<&TypeDecl>>,
) -> (String, String) {
    let mut start = String::new();
    let mut end = String::new();
    let _ = writeln!(start, "// @generated by ntbindgen.  Do not edit.");
    let _ = writeln!(start, "// clang-format off");
    let _ = writeln!(start, "#if !IN_PARSER");

    // Nested anons contribute their own `_mNNN` shims too -- merge all
    // bodies into the parent's magic file so a single include of the
    // parent's magic.start brings every nested macro into scope.
    //
    // Inlined anon types live in the parent's class scope (not in
    // their declared namespace), so a field whose type names one of
    // them must reference the bare name -- pre-build the set and pass
    // it through to `emit_macro_line`.
    let inlined_paths: rustc_hash::FxHashSet<(String, String)> = anons_by_parent
        .get(parent_original)
        .map(|a| {
            a.iter()
                .map(|t| match t {
                    TypeDecl::Struct(s) | TypeDecl::Union(s) => {
                        (s.path.ns.clone(), s.path.name.clone())
                    },
                    TypeDecl::Enum(e) => (e.path.ns.clone(), e.path.name.clone()),
                    TypeDecl::Stub(s) => (s.path.ns.clone(), s.path.name.clone()),
                })
                .collect()
        })
        .unwrap_or_default();
    let mut counter = 0u32;
    let anons = anons_by_parent.get(parent_original);
    if let Some(anons) = anons {
        for anon in anons {
            let inner_fields: &[FieldDecl] = match anon {
                TypeDecl::Struct(c) | TypeDecl::Union(c) => &c.fields,
                TypeDecl::Enum(_) | TypeDecl::Stub(_) => continue,
            };
            for f in inner_fields {
                emit_macro_line(&mut start, counter, f, &inlined_paths);
                counter += 1;
            }
        }
    }
    for f in fields {
        emit_macro_line(&mut start, counter, f, &inlined_paths);
        counter += 1;
    }

    let _ = writeln!(start, "#else");
    for i in 0..counter {
        let _ = writeln!(start, "#define _m{i:03}");
    }
    let _ = writeln!(start, "#endif");

    for i in 0..counter {
        let _ = writeln!(end, "#undef _m{i:03}");
    }
    let end = format!("// @generated by ntbindgen.  Do not edit.\n// clang-format off\n{end}");
    let _ = parent;
    (start, end)
}

fn emit_macro_line(
    out: &mut String,
    counter: u32,
    f: &FieldDecl,
    inlined: &rustc_hash::FxHashSet<(String, String)>,
) {
    let identifier = &f.identifier;
    let ty = render_field_type_cpp_scoped(f, inlined);
    if let Some(bf) = &f.bitfield {
        let storage_type = match bf.storage_bytes {
            1 => "uint8_t",
            2 => "uint16_t",
            4 => "uint32_t",
            8 => "uint64_t",
            _ => "uint32_t",
        };
        let _ = writeln!(
            out,
            "#define _m{c:03} _SDK_MAGIC_BITFIELD(nop_t, _SDK_ESCAPE({ty}), \"{ident}\", {name}, 0x{boff:x}, 0x{bsize:x}, true, 0x{key:x}, {bsize_dec}, {storage})",
            c = counter,
            ty = ty,
            ident = identifier,
            name = f.rust_name,
            boff = f.bit_offset,
            bsize = f.bit_size,
            bsize_dec = f.bit_size,
            key = f.key,
            storage = storage_type,
        );
    } else {
        let _ = writeln!(
            out,
            "#define _m{c:03} _SDK_MAGIC_PROPERTY(_SDK_ESCAPE({ty}), \"{ident}\", {name}, 0x{boff:x}, 0x{bsize:x}, true, 0x{key:x})",
            c = counter,
            ty = ty,
            ident = identifier,
            name = f.rust_name,
            boff = f.bit_offset,
            bsize = f.bit_size,
            key = f.key,
        );
    }
}

fn ns_to_cpp(ns: &str) -> String {
    ns.to_owned()
}

fn ns_open(ns: &str) -> String {
    let mut s = String::new();
    for seg in ns.split("::") {
        let _ = writeln!(s, "namespace {seg}");
        let _ = writeln!(s, "{{");
    }
    s
}

fn ns_close(ns: &str) -> String {
    let mut s = String::new();
    for _ in ns.split("::") {
        let _ = writeln!(s, "}};");
    }
    s
}

fn summary_key(parent_original: &str, opts: EmitOptions) -> u64 {
    if opts.no_encrypt { 0 } else { crate::name::sdbm_hash(&format!("{parent_original}.$")) }
}

fn render_enum(e: &EnumDecl) -> String {
    let mut out = String::new();
    let _ = writeln!(out, "// @generated by ntbindgen.  Do not edit.");
    let _ = writeln!(out, "// clang-format off");
    let _ = writeln!(out, "#pragma once");
    let _ = writeln!(out, "#include <ntbind/support_library.hpp>");
    let _ = writeln!(out);
    let _ = write!(out, "{}", ns_open(&e.path.ns));
    let _ = writeln!(out, "    // [enum {}]", e.original_name);
    let _ = writeln!(out, "    //  Windows 10 v20H2");
    let _ = writeln!(out, "    //");
    let underlying = render_cpp_type(&e.underlying);
    let _ = writeln!(out, "    enum class {} : {}", e.path.name, underlying);
    let _ = writeln!(out, "    {{");
    // Allow duplicate values directly in `enum class`.  Sort variants by
    // (value, name) ascending.  Alignment widths are computed against the
    // sorted set so the `=` column stays stable.
    let mut sorted: Vec<&_> = e.variants.iter().collect();
    sorted.sort_by(|a, b| a.value.cmp(&b.value).then_with(|| a.rust_name.cmp(&b.rust_name)));
    let max_name = sorted.iter().map(|v| v.rust_name.chars().count()).max().unwrap_or(0);
    for v in &sorted {
        let _ = writeln!(
            out,
            "        {nm:<w$} = 0x{val:x},    // Windows 10 v20H2",
            nm = format!("{} ", v.rust_name),
            w = max_name + 1,
            val = v.value,
        );
    }
    let _ = writeln!(out, "    }};");
    let _ = write!(out, "{}", ns_close(&e.path.ns));
    out
}

// Render a [`TypeRef`] for the C++ side:
// - primitives use `uintN_t` / `intN_t` (no `u32`/`i32`)
// - bitfield underlying widths become `uintN_t` for arbitrary N
// - pointers collapse to `void*` (we don't carry pointee types in IR)
// - arrays render as `sdk::array<T, N>`
// - opaque storage renders as `uint8_t[N]` literally; user shouldn't see
//   these in practice (anon synthesis handles most cases)
//
fn render_cpp_type(t: &TypeRef) -> String {
    match t {
        TypeRef::Primitive(p) => primitive_to_cpp(p).to_owned(),
        TypeRef::Pointer => "void*".to_owned(),
        TypeRef::TypedPointer { path, kind } => {
            let kw = match kind {
                crate::ir::PointeeKind::Struct => "struct",
                crate::ir::PointeeKind::Union => "union",
                crate::ir::PointeeKind::Enum => "enum",
            };
            format!("{kw} {}::{}*", path.ns, path.name)
        },
        TypeRef::UserDefined(path) => format!("{}::{}", path.ns, path.name),
        TypeRef::External { cpp_path, .. } => (*cpp_path).to_owned(),
        TypeRef::Array { element, count } => {
            format!("sdk::array<{}, {}>", render_cpp_type(element), count)
        },
        TypeRef::Opaque(s) => format!("sdk::array<uint8_t, 0x{:x}>", s.max(&1)),
        // Pointer to a non-named pointee -- recurse and append `*`.
        TypeRef::Ref(inner) => format!("{}*", render_cpp_type(inner)),
        // Pointer to a function -- bake the pointer into the
        // `sdk::function<R(args)>*` form Selene uses.
        TypeRef::FnPtr(sig) => render_fn_ptr_cpp(sig),
    }
}

// Render a field's *visible* C++ type, branching on bitfield (uses
// `uintN_t` of the field's actual bit_size, not the storage word).
//
fn render_field_type_cpp(f: &FieldDecl) -> String {
    render_field_type_cpp_scoped(f, &Default::default())
}

// Same as [`render_field_type_cpp`] but strips the namespace prefix from
// any `UserDefined` reference matching the `inlined` set -- those types
// are declared *inside* the parent struct's scope, so the bare name is
// the only one that resolves at the field-declaration site.
//
fn render_field_type_cpp_scoped(
    f: &FieldDecl,
    inlined: &rustc_hash::FxHashSet<(String, String)>,
) -> String {
    if let Some(bf) = &f.bitfield {
        let prefix = if bf.signed { "int" } else { "uint" };
        return format!("{prefix}{}_t", f.bit_size.max(1));
    }
    if let TypeRef::UserDefined(p) = &f.ty
        && inlined.contains(&(p.ns.clone(), p.name.clone()))
    {
        return p.name.clone();
    }
    render_cpp_type(&f.ty)
}

fn primitive_to_cpp(p: &str) -> &'static str {
    match p {
        "i8" => "int8_t",
        "u8" => "uint8_t",
        "i16" => "int16_t",
        "u16" => "uint16_t",
        "i32" => "int32_t",
        "u32" => "uint32_t",
        "i64" => "int64_t",
        "u64" => "uint64_t",
        "i128" => "__int128",
        "u128" => "unsigned __int128",
        "f32" => "float",
        "f64" => "double",
        "::core::ffi::c_void" => "void",
        _ => "uint64_t",
    }
}

fn write_api(include_root: &Path, ns: &MergedNamespace) -> Result<()> {
    let ns_dir: PathBuf =
        ns.default_ns.split("::").fold(include_root.to_path_buf(), |p, seg| p.join(seg));
    fs::create_dir_all(ns_dir.join("magic"))?;

    let mut body = String::new();
    let _ = writeln!(body, "// @generated by ntbindgen.  Do not edit.");
    let _ = writeln!(body, "// clang-format off");
    let _ = writeln!(body, "#pragma once");
    let _ = writeln!(body, "#include <ntbind/support_library.hpp>");
    let _ = writeln!(body);
    let _ = writeln!(body, "#include \"magic/api.start.hpp\"");
    let _ = write!(body, "{}", ns_open(ns.default_ns));

    let mut start = String::new();
    let mut end = String::new();
    let _ = writeln!(start, "// @generated by ntbindgen.  Do not edit.");
    let _ = writeln!(start, "// clang-format off");
    let _ = writeln!(start, "#if !IN_PARSER");

    let mut used = rustc_hash::FxHashSet::default();
    let mut visible_index = 0u32;
    for p in &ns.publics {
        let mut name = p.rust_name.clone();
        while !used.insert(name.clone()) {
            name.push('_');
        }
        let _ = writeln!(body, "    // [{}]", p.original_name);
        let _ = writeln!(body, "    // Ldr = [{}]", ns.hint_image);
        let _ = writeln!(body, "    // => Windows 10 v20H2");
        let _ = writeln!(body, "    // {} .rdata:0x{:x}, 0x10 bytes", ns.hint_image, p.rva);
        let _ = writeln!(body, "    //");
        let public_ty = public_ty_cpp(p);
        let _ = writeln!(body, "    _m{vi}({public_ty}) {name};", vi = visible_index);
        let _ = writeln!(body);

        let _ = writeln!(
            start,
            "#define _m{vi}(...) _SDK_MAGIC_LINK(_SDK_ESCAPE(__VA_ARGS__), \"${orig}${img}\", 0x{rva:x}, 0x0, true, 0x{key:x})",
            vi = visible_index,
            orig = p.original_name,
            img = ns.hint_image,
            rva = p.rva,
            key = p.key,
        );
        visible_index += 1;
    }
    let _ = write!(body, "{}", ns_close(ns.default_ns));
    let _ = writeln!(body, "#include \"magic/api.end.hpp\"");

    let _ = writeln!(start, "#else");
    for i in 0..visible_index {
        let _ = writeln!(start, "#define _m{i}(...) __VA_ARGS__");
    }
    let _ = writeln!(start, "#endif");
    for i in 0..visible_index {
        let _ = writeln!(end, "#undef _m{i}");
    }
    let end = format!("// @generated by ntbindgen.  Do not edit.\n// clang-format off\n{end}");

    fs::write(ns_dir.join("api.hpp"), body)?;
    fs::write(ns_dir.join("magic").join("api.start.hpp"), start)?;
    fs::write(ns_dir.join("magic").join("api.end.hpp"), end)?;
    Ok(())
}

// Renders the type slot for `_m<N>(<TYPE>) name;` -- either a typed
// `sdk::function<R(args...)>*` recovered from the PDB's IPI stream, or
// the catch-all `sdk::unknown_ptr` when no signature is available.
//
// Unlike the Rust side, the C++ form CAN express variadics directly --
// `sdk::function<R(args..., ...)>*` Just Works under the C++ runtime
// types in `cpp/include/ntbind/runtime.hpp`.
fn public_ty_cpp(p: &crate::ir::PublicDecl) -> String {
    match &p.signature {
        Some(sig) => render_fn_ptr_cpp(sig),
        None => "sdk::unknown_ptr".to_owned(),
    }
}

// Renders a `FnSig` as a C++ `sdk::function<R(args...)>*` type.
fn render_fn_ptr_cpp(sig: &FnSig) -> String {
    let mut s = String::with_capacity(64);
    s.push_str("sdk::function<");
    s.push_str(&render_cpp_type(&sig.return_type));
    s.push('(');
    for (i, p) in sig.params.iter().enumerate() {
        if i != 0 {
            s.push_str(", ");
        }
        s.push_str(&render_cpp_type(p));
    }
    if sig.variadic {
        if !sig.params.is_empty() {
            s.push_str(", ");
        }
        s.push_str("...");
    }
    s.push_str(")>*");
    s
}
